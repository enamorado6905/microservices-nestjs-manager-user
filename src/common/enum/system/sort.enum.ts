/**
 * Enum representing the fields by which sorting can be performed.
 */
export enum SortEnum {
  UPDATED_AT = 'updated_at', // Represents sorting by the 'updated_at' field.
  CREATED_AT = 'created_at', // Represents sorting by the 'created_at' field.
}

/**
 * Enum representing the direction of sorting.
 */
export enum SortDescEnum {
  DESC = 'DESC', // Represents descending sort order.
  ASC = 'ASC', // Represents ascending sort order.
}
